<h4>Entry requirements</h4>

Certificate Programmes: Candidates must have obtained Uganda Certificate of Education with at least five passes at one
sitting or its equivalent.
Diploma Programmes: The minimum requirement is a principal pass in the Uganda Advanced Certificate of Education in the
subjects relevant to the programme applied for, or a certificate in related field.
Undergraduate Programmes: A minimum of two principal passes in the Uganda Advanced Certificate of Education obtained at
the same sitting or its equivalent. Diploma holders are also invited to apply. For all those applying for distance
learning, relevant certificates are required.
Postgraduate Programmes: At least a Second Class Honours degree from recognized Institutions in a relevant field or in
exceptional circumstances, professional qualifications will be required. Candidates are required to pass the UMU
Graduate Admission Test (GAT) and have at least two years of working experience in the relevant field.

